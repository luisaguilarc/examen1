    <h1>Adolescencia media (14-17 años):</h1>
    <p>El adolescente ya está casi completamente desarrollado, sus órganos sexuales están listos para la reproducción y el deseo sexual se incrementa. En esta fase se desarrolla una sensación de invulnerabilidad y fortaleza que inducen al joven a comportarse de una forma narcisista. Se empieza a buscar el contacto habitual con el otro sexo y pueden llegar las primeras relaciones sexuales. </p>
