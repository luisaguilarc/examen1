    <p>Los cambios que se producen en la sexualidad pueden enmarcarse dentro de las etapas de la adolescencia y la pubertad, ya que las transformaciones físicas y mentales condicionan sobremanera el desarrollo de la sexualidad y de la identidad sexual.</p>
