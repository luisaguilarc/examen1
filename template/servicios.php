    <h1>Adolescencia tardía (17-21 años): </h1>
    <p>El adolescente se ha desarrollado por completo, física y psíquicamente. La capacidad de pensar en abstracto y de ser consciente de las consecuencias futuras de los actos hace que el joven pueda mantener relaciones sexuales maduras y seguras. El deseo ya no sólo responde a un estímulo o pulsión sexual, sino que el adolescente, ya adulto, comienza a buscar otros valores en sus relaciones sociales, como la confianza o la reciprocidad.</p>
