    <p>Si decides tener relaciones sexuales, es importante que tengas información sobre los métodos anticonceptivos, las infecciones y las emociones o sentimientos. Las decisiones sobre cuándo comenzar a ser sexualmente activo, cómo protegerte contra las ITS y cómo evitar un embarazo dependen de ti. Estas son decisiones importantes y vale la pena hablar sobre ellas con los adultos que se preocupan por ti, incluido tu médico.</p>
   
    
