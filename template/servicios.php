    <h1>Adolescencia temprana (11-13 años):.</h1>
    <p>Durante esta etapa, que se caracteriza por la velocidad de los cambios físicos en el adolescente, el joven se encuentra aún lejos del deseo sexual adulto, por lo que se presenta como una fase de autoexploración (a menudo a través de la masturbación), debido a los nuevos cambios físicos y psíquicos que experimenta (como la aparición de los primeros impulsos sexuales y de la espermarquia o primera eyaculación), y también de exploración del contacto con el otro sexo..</p>
